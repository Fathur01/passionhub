package com.fwd.ws.response;

import java.util.List;
import java.util.Map;

public class DropdownListResponse extends WebServiceResponse {
	
	private List<DropdownItem> resultList;
	private Map<String,String> translationMap;

	public List<DropdownItem> getResultList() {
		return resultList;
	}

	public void setResultList(List<DropdownItem> resultList) {
		this.resultList = resultList;
	}

	public Map<String, String> getTranslationMap() {
		return translationMap;
	}

	public void setTranslationMap(Map<String, String> translationMap) {
		this.translationMap = translationMap;
	}


}
