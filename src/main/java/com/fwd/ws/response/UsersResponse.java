package com.fwd.ws.response;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

import com.fwd.model.passion.Users;

@XmlRootElement
public class UsersResponse  extends WebServiceResponse{
	
	private List<Users> usersList;
	private List<DropdownItem> usersDropList;
	
	public List<Users> getUsersList() {
		return usersList;
	}
	public void setUsersList(List<Users> usersList) {
		this.usersList = usersList;
	}
	public List<DropdownItem> getUsersDropList() {
		return usersDropList;
	}
	public void setUsersDropList(List<DropdownItem> usersDropList) {
		this.usersDropList = usersDropList;
	}
	
		
}
