package com.fwd.ws.response;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

import com.fwd.model.passion.News;

@XmlRootElement
public class NewsResponse  extends WebServiceResponse{
	
	private List<News> newsList;
	private List<DropdownItem> newsDropList;
	
	public List<News> getNewsList() {
		return newsList;
	}
	public void setNewsList(List<News> newsList) {
		this.newsList = newsList;
	}
	public List<DropdownItem> getNewsDropList() {
		return newsDropList;
	}
	public void setNewsDropList(List<DropdownItem> newsDropList) {
		this.newsDropList = newsDropList;
	}
	
		
}
